_TRANSPOSE_TEMPLATE = """# Script.Name = "MCP Apply"
# Script.Category = "MCP"
# AUTO-GENERATED by fl-studio-mcp (transpose). Do not edit -- overwritten.

import flpianoroll as flp

SEMITONES = {semitones!r}


def _run():
    score = flp.score
    notes = []
    for i in range(score.noteCount):
        n = score.getNote(i)
        new_pitch = max(0, min(127, n.number + SEMITONES))
        notes.append((new_pitch, n.time, n.length, n.velocity))
    if not notes:
        return
    try:
        score.clearNotes(True)
    except TypeError:
        score.clearNotes()
    for num, t, ln, vel in notes:
        m = flp.Note()
        m.number = num
        m.time = t
        m.length = ln
        m.velocity = vel
        score.addNote(m)


def _go():
    undo = getattr(flp.score, "undoSection", None)
    if callable(undo):
        try:
            with undo():
                _run()
            return
        except Exception:
            pass
    _run()


_go()
"""


def render_transpose_script(semitones: int) -> str:
    return _TRANSPOSE_TEMPLATE.format(semitones=int(semitones))


def write_transpose_script(semitones: int, scripts_dir: str | None = None) -> str:
    """Write the transpose script to MCP_Apply.pyscript. Returns path."""
    scripts_dir = scripts_dir or PIANO_ROLL_SCRIPTS_DIR
    text = render_transpose_script(semitones)
    path = os.path.join(scripts_dir, APPLY_SCRIPT_NAME)
    with open(path, "w", encoding="ascii") as f:
        f.write(text)
    return path
