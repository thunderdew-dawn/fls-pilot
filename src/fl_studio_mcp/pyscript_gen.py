# Quantize
# ---------------------------------------------------------------------------


def quantize_notes(notes, grid_bars, snap_ends=False):
    """Snap each note's start (and optionally length) to a grid, in BARS. PURE.

    grid_bars: 0.0625 = 1/16, 0.125 = 1/8, 0.25 = 1/4. grid_bars <= 0 -> unchanged.
    Used to quantize notes we GENERATE before writing them.
    """
    g = float(grid_bars or 0)
    if g <= 0:
        return [dict(n) for n in notes]
    out = []
    for n in notes:
        m = dict(n)
        m["time_bars"] = round(round(float(n.get("time_bars", 0.0)) / g) * g, 6)
        if snap_ends:
            m["length_bars"] = max(g, round(round(float(n.get("length_bars", g)) / g) * g, 6))
        out.append(m)
    return out


# Quantizes the notes ALREADY in the open piano roll: read every note, clear,
# re-add with start (and optionally length) snapped to the grid. Uses the same
# addNote path the apply script proved works. PPQ-agnostic (uses live score.PPQ).
_QUANTIZE_TEMPLATE = """# Script.Name = "MCP Apply"
# Script.Category = "MCP"
# AUTO-GENERATED by fl-studio-mcp (quantize). Do not edit -- overwritten.

import flpianoroll as flp

GRID_BARS = {grid!r}     # snap resolution in bars (0.0625 = 1/16)
SNAP_ENDS = {ends!r}     # also snap note lengths to the grid


def _run():
    score = flp.score
    step = max(1, int(round(GRID_BARS * score.PPQ * 4)))
    notes = []
    for i in range(score.noteCount):
        n = score.getNote(i)
        notes.append((n.number, n.time, n.length, n.velocity))
    if not notes:
        return
    try:
        score.clearNotes(True)
    except TypeError:
        score.clearNotes()
    for num, t, ln, vel in notes:
        m = flp.Note()
        m.number = num
        m.time = int(round(t / step) * step)
        m.length = max(step, int(round(ln / step) * step)) if SNAP_ENDS else ln
        m.velocity = vel
        score.addNote(m)


def _go():
    undo = getattr(flp.score, "undoSection", None)
    if callable(undo):
        try:
            with undo():
                _run()
            return
        except Exception:
            pass
    _run()


_go()
"""


def render_quantize_script(grid_bars, snap_ends=False):
    return _QUANTIZE_TEMPLATE.format(grid=float(grid_bars), ends=bool(snap_ends))


def write_quantize_script(grid_bars, snap_ends=False, scripts_dir=None):
    """Write the quantize-existing-notes script to MCP_Apply.pyscript. Returns path."""
    scripts_dir = scripts_dir or PIANO_ROLL_SCRIPTS_DIR
    text = render_quantize_script(grid_bars, snap_ends)
    path = os.path.join(scripts_dir, APPLY_SCRIPT_NAME)
    with open(path, "w", encoding="ascii") as f:
        f.write(text)
    return path


